import os
import ctypes

MY_DIR = os.path.dirname(os.path.abspath(__file__))

class Kuiper(object):
    def __init__(self):
        self.__kuiper_lib = ctypes.cdll.LoadLibrary(os.path.join(MY_DIR, 'libkuiper.so'))
        self.__kuiper_lib.Kuiper_kptwo.restype = (ctypes.c_double)
        self.obj = self.__kuiper_lib.Kuiper_new()

    def kptwo(self,data1,data2):
        cdata1 = (ctypes.c_double*len(data1))(*data1)
        cdata2 = (ctypes.c_double*len(data2))(*data2)
        ctypes.cast(cdata1, ctypes.POINTER(ctypes.c_double))
        ctypes.cast(cdata2, ctypes.POINTER(ctypes.c_double))
        n1 = ctypes.c_int(len(data1))
        n2 = ctypes.c_int(len(data2))
        return self.__kuiper_lib.Kuiper_kptwo(self.obj,cdata1,cdata2,n1,n2)

